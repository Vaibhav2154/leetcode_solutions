class Solution:
    def lengthOfLongestSubstring(self, s: str) -> int:
        # Set to track the duplicate elements
        seen = set()
        left, res = 0,0,
        for right in range(len(s)):
            while s[right] in seen:
                seen.remove(s[left])
                left +=1
            seen.add(s[right])
            res = max(res, right - left + 1)
            # print(seen)
        return res